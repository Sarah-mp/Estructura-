class Vehículo:
    def __init__(self, marca, combustible):
        self.marca = marca
        self.combustible = combustible

    def encender(self):
        print("El vehículo está encendido.")

    def arrancar(self):
        print("El vehículo está arrancando.")

    def __str__(self):
        return f"Vehículo de marca {self.marca}, con combustible {self.combustible}."


# Instanciar la clase Vehículo
mi_vehiculo = Vehículo("Toyota", "gasolina")

# Usar el método mágico __str__ para imprimir la marca y el combustible
print(mi_vehiculo)
